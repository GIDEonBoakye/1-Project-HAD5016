def find_best_threshold(y_true, y_prob, thresholds=None, metric="f1"):
    """
    Find the best probability threshold based on chosen metric.
    metric can be: 'f1', 'recall', 'precision'
    """
    if thresholds is None:
        thresholds = np.arange(0.05, 0.96, 0.01)

    best_threshold = 0.5
    best_score = -1
    history = []

    for thr in thresholds:
        y_pred_thr = (y_prob >= thr).astype(int)

        if metric == "f1":
            score = f1_score(y_true, y_pred_thr, zero_division=0)
        elif metric == "recall":
            score = recall_score(y_true, y_pred_thr, zero_division=0)
        elif metric == "precision":
            score = precision_score(y_true, y_pred_thr, zero_division=0)
        else:
            raise ValueError("metric must be one of: 'f1', 'recall', 'precision'")

        prec = precision_score(y_true, y_pred_thr, zero_division=0)
        rec = recall_score(y_true, y_pred_thr, zero_division=0)
        f1 = f1_score(y_true, y_pred_thr, zero_division=0)

        history.append({
            "threshold": thr,
            "precision": prec,
            "recall": rec,
            "f1": f1,
            "score_used_for_selection": score
        })

        if score > best_score:
            best_score = score
            best_threshold = thr

    return best_threshold, best_score, history


