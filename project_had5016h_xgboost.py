# -----------------------------
# 4) Outlier counts using IQR rule (numeric report)
# -----------------------------
def iqr_outlier_count(series: pd.Series):
    s = series.dropna()
    if len(s) < 4:
        return 0, np.nan, np.nan
    q1 = s.quantile(0.25)
    q3 = s.quantile(0.75)
    iqr = q3 - q1
    lo = q1 - 1.5 * iqr
    hi = q3 + 1.5 * iqr
    outliers = ((s < lo) | (s > hi)).sum()
    return int(outliers), lo, hi

