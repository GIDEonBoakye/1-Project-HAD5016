    print("\n" + "=" * 75)
    print(f"Strategy: {name}")
    print(f"Best CV Recall : {info['best_cv_recall']:.4f}")
    print(f"Best Params    : {info['best_params']}")

    tn, fp, fn, tp = info['tn_fp_fn_tp']
