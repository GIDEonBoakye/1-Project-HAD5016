# sklearn preprocessing & pipeline
from sklearn.experimental import enable_iterative_imputer   # noqa: F401
from sklearn.impute import IterativeImputer, SimpleImputer
from sklearn.preprocessing import StandardScaler
from sklearn.pipeline import Pipeline
from sklearn.compose import ColumnTransformer

# sklearn model selection & metrics
from sklearn.model_selection import (
    train_test_split,
    StratifiedKFold,
    GridSearchCV,
)
from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score,
    roc_auc_score,
    roc_curve,
    confusion_matrix,
    ConfusionMatrixDisplay,
    classification_report,
)

# Gradient boosting
from sklearn.ensemble import GradientBoostingClassifier

# imbalanced-learn (mirrors GaussianNB section style)
from imblearn.pipeline import Pipeline as ImbPipeline
from imblearn.over_sampling import RandomOverSampler, SMOTE
from imblearn.under_sampling import RandomUnderSampler
from imblearn.combine import SMOTEENN

x_cont = X_train.columns.tolist()

preprocessor = ColumnTransformer(
    transformers=[
        ('num', Pipeline([
            ('imputer', IterativeImputer(random_state=42)),
        ]), x_cont),
    ],
    remainder='drop',    # Explicitly drop any columns not handled by transformers
)

# Resampling strategies
strategies = {
    "1_no_resampling":          None,
    "2_oversample_minority":    RandomOverSampler(random_state=42),
    "3_undersample_majority":   RandomUnderSampler(random_state=42),
    "4_over_and_under_combined": SMOTEENN(random_state=42),
}

# Hyperparameter grid for GradientBoostingClassifier


param_grid = {
    "xg_boost__n_estimators": [25, 50, 100],
    "xg_boost__max_depth":[1, 3, 5, 7],
    "xg_boost__learning_rate":[0.05,0.1],
    "xg_boost__min_samples_leaf":[1, 5, 10], # sklearn analog to min_child_weight
}
# CV setup — 5 folds as used throughout the project
skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

#  Grid search across all four resampling strategies
xgb_results = {}

for name, sampler in strategies.items():

    # Build pipeline steps dynamically (mirrors Logistic Regression section)
    steps = [("preprocessor", preprocessor)]

    if sampler is not None:
        # Special handling for SMOTEENN to adjust k_neighbors if necessary
        if isinstance(sampler, SMOTEENN):
            steps.append(("sampler", SMOTEENN(random_state=42, smote=SMOTE(k_neighbors=3, random_state=42))))
        else:
            steps.append(("sampler", sampler))

    steps.append(("xg_boost", GradientBoostingClassifier(random_state=42)))

    # Use ImbPipeline so resamplers work correctly inside cross-validation
    model = ImbPipeline(steps=steps)

    # Grid search
    grid = GridSearchCV(
        estimator=model,
        param_grid=param_grid,
        cv=skf,
        scoring='recall',       # Prioritising recall: missing a DFU case is costly
        n_jobs=-1,
        verbose=0,
        refit=True,
    )

    grid.fit(X_train, y_train)

    # ── Predictions ──────────────────────────────────────────────────────
    best_model = grid.best_estimator_
    y_pred     = best_model.predict(X_test)
    y_prob     = best_model.predict_proba(X_test)[:, 1]

    # ── Metrics ──────────────────────────────────────────────────────────
    cm         = confusion_matrix(y_test, y_pred)
    tn, fp, fn, tp = cm.ravel()
    auc        = roc_auc_score(y_test, y_prob)
    fpr, tpr, _ = roc_curve(y_test, y_prob)

    xgb_results[name] = {
        "best_params":   grid.best_params_,
        "best_cv_recall": grid.best_score_,
        "confusion_matrix": cm,
        "tn_fp_fn_tp":   (tn, fp, fn, tp),
        "test_report":   classification_report(y_test, y_pred, digits=4),
        "auc":           auc,
        "roc":           (fpr, tpr),
        "best_estimator": best_model,
    }



# =========================================================
