    ct = pd.crosstab(data[target_col], data["_any_use"])
    ct.columns = ["No use", "Any use"]
    ct.index   = ["No DFU", "DFU"]

    prop_no_dfu = ct.loc["No DFU", "Any use"] / ct.loc["No DFU"].sum()
    prop_dfu    = ct.loc["DFU",    "Any use"] / ct.loc["DFU"].sum()

    # --- Chi-squared test (binary association) ---
    chi2, p_chi2, dof, expected = chi2_contingency(ct)

    # --- Relative risk ---
    rr = prop_dfu / prop_no_dfu if prop_no_dfu > 0 else np.nan

    # --- Odds ratio ---
